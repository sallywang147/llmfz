extern "C" int LLVMFuzzerTestOneInput(const uint8_t* data, size_t size) {
  FuzzedDataProvider stream(data, size);
  const char* format_string = stream.ConsumeRemainingBytesAsString().c_str();
  gzprintf(nullptr, format_string, nullptr);
  return 0;
}